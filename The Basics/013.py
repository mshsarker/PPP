#  013 Ask the user to enter a number that is under 20. If they enter a number that is 20 or more, display the message “Too high”, otherwise display “Thank you”.
T20 = int(input("Please write an integer below 20\n"))
if T20 > 20:
	print("Too high")
else:
	print("Thank you!!")
